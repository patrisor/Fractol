# Zero is black
def getColors():
    ret = ["\033[1;91;40m"]
    for i in range(31, 38, 1):
        ret.append("\033[1;" + str(i) + ";40m")
    ret.append("\033[1;97;40m")
    ret.append('')
    return ret
